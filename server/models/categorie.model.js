import mongoose from "mongoose";

const categorieSchema = new mongoose.Schema(
  {
    name: {
      type: String,
      required: true,
      unique: true,
      trim: true,
    },
    description: {
      type: String,
      required: true,
      trim: true,
    },
  },
  { timestamps: true }
);

const Categorie = mongoose.model("Categorie", categorieSchema);
export default Categorie;
